#define NGX_CMD_CLOSE_CHANNEL  2
#define NGX_CMD_QUIT           3
#define NGX_CMD_TERMINATE      4
#define NGX_CMD_REOPEN         5

// 这几个宏定义是全局变量ngx_process的可能值
// 这里用一个枚举会不会更清晰一些？
